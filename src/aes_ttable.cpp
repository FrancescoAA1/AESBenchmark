// ===== Key expansion =====
void AESTTable::keyExpansion(const std::vector<uint8_t>& key) {
    // TODO: implement AES-128 key schedule (44 words)
    roundKeys.resize(44);

    for(int i = 0; i < 4; i++){
        roundKeys[i] = (key[4*i] << 24) | (key[4*i+1] << 16)
                    | (key[4*i+2] << 8) | (key[4*i+3]);
    }
 // Expand res:t: AES key schedule use round constants(Rcon).
//Rcon[1] = 0x01
//Rcon[2] = 0x02
//Rcon[3] = 0x04
//doubling each time in GF(2^8), with the irreducible polynomial
//x^8 + x^4 + x^3 + x + 1
//Special cases when it overflows → wrap with 0x1B, 0x36 
//0x100⊕0x11B=0x1B
    static const uint32_t Rcon[10] = {
        0x01000000,0x02000000,0x04000000,0x08000000,0x10000000,
        0x20000000,0x40000000,0x80000000,0x1B000000,0x36000000
    };
    for (int i = 4; i < 44; i++) {
        uint32_t temp = roundKeys[i-1];
        if (i % 4 == 0) {
            // RotWord
            temp = (temp << 8) | (temp >> 24);

            // SubWord
            temp = (S[(temp >> 24) & 0xFF] << 24) |
                (S[(temp >> 16) & 0xFF] << 16) |
                (S[(temp >>  8) & 0xFF] << 8) |
                (S[temp & 0xFF]);

            // XOR Rcon
            temp ^= Rcon[(i/4) - 1];
        }
        roundKeys[i] = roundKeys[i-4] ^ temp;
    }


}

// ===== Encrypt block =====
void AESTTable::encryptBlock(const uint8_t in[16], uint8_t out[16]) const {
    // TODO:
    // 1. Load input into s0..s3
    // 2. Initial AddRoundKey
    // 3. Rounds 1..9 with T-tables
    // 4. Final round with S-box + ShiftRows + AddRoundKey
    // 5. Store output
// Load state into 4 words
    uint32_t s0 = (in[0]<<24)|(in[1]<<16)|(in[2]<<8)|in[3];
    uint32_t s1 = (in[4]<<24)|(in[5]<<16)|(in[6]<<8)|in[7];
    uint32_t s2 = (in[8]<<24)|(in[9]<<16)|(in[10]<<8)|in[11];
    uint32_t s3 = (in[12]<<24)|(in[13]<<16)|(in[14]<<8)|in[15];

    // Initial round key
    s0 ^= roundKeys[0];
    s1 ^= roundKeys[1];
    s2 ^= roundKeys[2];
    s3 ^= roundKeys[3];

    // 9 main rounds (T-tables)
    for (int round = 1; round < 10; round++) {
        uint32_t t0 =
            T0[s0 >> 24] ^
            T1[(s1 >> 16) & 0xFF] ^
            T2[(s2 >> 8) & 0xFF] ^
            T3[s3 & 0xFF] ^
            roundKeys[4*round + 0];

        uint32_t t1 =
            T0[s1 >> 24] ^
            T1[(s2 >> 16) & 0xFF] ^
            T2[(s3 >> 8) & 0xFF] ^
            T3[s0 & 0xFF] ^
            roundKeys[4*round + 1];

        uint32_t t2 =
            T0[s2 >> 24] ^
            T1[(s3 >> 16) & 0xFF] ^
            T2[(s0 >> 8) & 0xFF] ^
            T3[s1 & 0xFF] ^
            roundKeys[4*round + 2];

        uint32_t t3 =
            T0[s3 >> 24] ^
            T1[(s0 >> 16) & 0xFF] ^
            T2[(s1 >> 8) & 0xFF] ^
            T3[s2 & 0xFF] ^
            roundKeys[4*round + 3];

        s0 = t0; s1 = t1; s2 = t2; s3 = t3;
    }

    // Final round (no MixColumns, only S-box + ShiftRows + AddRoundKey)
    uint32_t o0 =
        (S[s0 >> 24] << 24) ^
        (S[(s1 >> 16) & 0xFF] << 16) ^
        (S[(s2 >> 8) & 0xFF] << 8) ^
        (S[s3 & 0xFF]) ^
        roundKeys[40];

    uint32_t o1 =
        (S[s1 >> 24] << 24) ^
        (S[(s2 >> 16) & 0xFF] << 16) ^
        (S[(s3 >> 8) & 0xFF] << 8) ^
        (S[s0 & 0xFF]) ^
        roundKeys[41];

    uint32_t o2 =
        (S[s2 >> 24] << 24) ^
        (S[(s3 >> 16) & 0xFF] << 16) ^
        (S[(s0 >> 8) & 0xFF] << 8) ^
        (S[s1 & 0xFF]) ^
        roundKeys[42];

    uint32_t o3 =
        (S[s3 >> 24] << 24) ^
        (S[(s0 >> 16) & 0xFF] << 16) ^
        (S[(s1 >> 8) & 0xFF] << 8) ^
        (S[s2 & 0xFF]) ^
        roundKeys[43];

    // Store output as 16 bytes
    out[0] = o0 >> 24; out[1] = o0 >> 16; out[2] = o0 >> 8; out[3] = o0;
    out[4] = o1 >> 24; out[5] = o1 >> 16; out[6] = o1 >> 8; out[7] = o1;
    out[8] = o2 >> 24; out[9] = o2 >> 16; out[10] = o2 >> 8; out[11] = o2;
    out[12] = o3 >> 24; out[13] = o3 >> 16; out[14] = o3 >> 8; out[15] = o3;
}
